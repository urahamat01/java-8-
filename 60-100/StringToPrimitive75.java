


public class StringToPrimitive75{

		public static void main(String[] args) {
			
			//string to primitive
			
	String i = "87";
	int s = Integer.parseInt(i);
	
	System.out.println("s = " + s);
	
	
	
	//string to any other datatype converts
	
	
	
	
	String o = "786";
	double k = Integer.parseInt(o);
	
	System.out.println("s = " + k);
	
	
	
	//string to any other datatype converts
	
	
	
	String u = "87";
	int m = Integer.parseInt(u);
	
	System.out.println("s = " + m);
	
	
	
	//string to any other datatype converts
	
	String h = "4236";
	int n = Integer.valueOf(h);
	
	System.out.println("s = " + n);
	
	
	
	
	
	
	
		}


}