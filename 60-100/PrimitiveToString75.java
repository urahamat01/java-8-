


public class PrimitiveToString75{

		public static void main(String[] args) {
			
			//primitive to string
			
	int i = 100;
	String s = Integer.toString(i);
	
	System.out.println("s = " + s);
	
	
	//double use to string 
	
	double a = 100.25;
	String h = Double.toString(a);
	
	System.out.println("s = " + h);
	
	//boolean to use able
	
	boolean l = true;
	String f = Boolean.toString(l);
	
	System.out.println("s = " + f);
	
	
	
	
		}


}